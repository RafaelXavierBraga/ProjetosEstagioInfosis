using System;
using System.Collections.Generic;
using System.Text;

namespace CalculadoraPosFixada
{
    public class Soma
    {
        /// <summary>
        /// Metodo que efetua a soma entre 2 valores.
        /// </summary>
        /// <param name="a">double</param>
        /// <param name="b">double</param>
        /// <returns>double</returns>
        public static double SomaNumeros(double a, double b) 
        {
            return a + b;
        }
    }
}
